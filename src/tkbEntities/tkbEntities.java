package tkbEntities;

public class tkbEntities {

	private String day;
	private String sc;
	private String ord;
	private String content;

	public void setDay(String value) {
		this.day = value;
	}

	public String getDay() {
		return day;
	}

	public void setSC(String value) {
		this.sc = value;
	}

	public String getSC() {
		return sc;
	}

	public void setOrd(String value) {
		this.ord = value;
	}

	public String getOrd() {
		return ord;
	}

	public void setContent(String value) {
		this.content = value;
	}

	public String getContent() {
		return content;
	}

}
